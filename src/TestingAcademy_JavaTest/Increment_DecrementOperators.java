package TestingAcademy_JavaTest;

public class Increment_DecrementOperators {
    public static void main(String[] args) {
        int i=10;
        int j=20;
        int k=30;
        int l=40;
        System.out.println(i++);
        System.out.println(j--);
        System.out.println(++k);
        System.out.println(--l);

    }

}
